                 Water-insoluble chromium(III) compounds and chromium metal are not considered a health hazard, while the toxicity and carcinogenic properties of chromium(VI) have been known for a long time.[1] An investigation into hexavalent chromium release into drinking water was the basis of the true story of Erin Brockovich, dramatised as Erin Brockovich (film).<br>

                          Because of the specific transport mechanisms, only limited amounts of chromium(III) enter the cells. Several in vitro studies indicated that high concentrations of chromium(III) in the cell can lead to DNA damage.[2] Acute oral toxicity ranges between 1900 and 3300 µg/kg.[3] The proposed beneficial effects of chromium(III) and its use in dietary supplements yielded some controversial results, but recent reviews suggest that moderate uptake of chromium(III) through dietary supplements poses no risk.[2]<br>

                          The World Health Organization-recommended maximum allowable concentration in drinking water for chromium (VI) is 0.05 milligrams per litre. [4] Hexavalent chromium is also one of the substances whose use is restricted by the European Restriction of Hazardous Substances Directive.           
