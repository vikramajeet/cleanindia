               Lead is a chemical element with atomic number 82 and symbol Pb (after the Latin plumbum). When freshly cut, it is bluish-white; it tarnishes to a dull gray upon exposure to air. It is a soft, malleable, and heavy metal with a density exceeding that of most common materials. It has the second highest atomic number of the non-radioactive elements and lies at the end of three major decay chains of heavier elements.<br><br>

                             Lead is a relatively inert post-transition metal. Its weak metallic character is illustrated by its amphoteric nature (lead and lead oxides react with both acids and bases) and tendency to form covalent bonds. Compounds of lead are usually found in the +2 oxidation state, rather than the +4 common with lighter members of the carbon group of the periodic table. Exceptions are mostly limited to organolead compounds. Like the lighter members of the group, lead exhibits a tendency to bond to itself; it can form chains, rings, and polyhedral structures.
