                <p>
                Q.3 What is banned from the landfill?
                <br>
                A. Almost any product with a circuit board is now banned from the landfill and considered universal waste and, as such,
                   requires special 
                   handling. Like used motor oil and paint, universal waste is a kind of hazardous waste. 
                  It is illegal to dispose of hazardous waste in the garbage. These products contain toxic substances, 
                  such as mercury, lead, and cadmium. Mercury, released into the environment through the improper disposal
                   of mercury-containing products, is a known neurotoxin.
                </p>
                 
                <p>
                Q.4. How do I recycle and dispose of electronic scrap?
                <br>
               A. Since electronic waste is subject to California’s hazardous waste regulations, it must be reused,
                 recycled, or disposed of properly. Please do not put these items in the garbage or curbside recycling bins. 
                 A complete list of universal waste products and information about disposal and recycling options, is available
                 on the DTSC website at
                 http://www.dtsc.ca.gov/HazardousWaste/UniversalWaste/index.cfm.
                 </p>
                 
                <p>
                Q.5. What is the Electronic Waste Recycling Act?
                <br>
                A. In 2003, the Electronic Waste Recycling Act established a funding system for the collection and recycling of
                  certain electronic wastes. Key elements of the Electronic Waste Recycling Act of 2003 include the collection of 
                  an electronic waste recycling fee at the retail point of sale of covered products and requirements on manufacturers
                  to provide consumer information regarding recycling opportunities. The intent of this program is to make recycling
                  options for consumers more convenient so products containing toxic compounds are disposed of appropriately. 
                  For more information on this law and its implementation, please seehttp://http://www.calrecycle.ca.gov/Electronics/.
                  <br>
                  Beginning in 2005, an Electronic Waste Recycling Fee will be assessed on certain "covered" 
                  electronic devices sold in California. "Covered" devices include most video display products,
                  such as computer monitors and televisions. The fee will be collected at the time of retail sale, 
                  including Internet and catalog sales, to California consumers and is not reimbursable to consumers.  
                </p>
                
                <p>
                Q. Are cathode ray tubes are hazardous waste?
                A. Computer monitors and televisions with Cathode Ray Tubes (CRTs) have been deemed hazardous waste by the California
                 Department of Toxic Substance Control and, therefore, cannot be disposed of in a landfill. Each CRT contains 5 to 7 pounds 
                 of lead, as well as cadmium, silver, gold, and other heavy metals and toxic compounds. Lab studies have shown the potential
                  for these compounds and elements to leach out of the computer equipment
                  into the landfill’s outflow and cause groundwater contamination.
