                Q.2. What do we do with e-waste?
                <br>
                A. Reduce, reuse, and recycle. Reduce your generation of e-waste through smart procurement and good 
                maintenance. Reuse still-functioning electronic equipment by donating or selling it to someone. 
                Recycle those components that cannot be repaired. 
                Most electronic devices contain a variety of materials, including metals, that can be recycled.
